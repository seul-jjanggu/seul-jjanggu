package chap01;

import java.util.Scanner;

public class 실습1_3 {
	public static void main(String[] args) {
		Scanner stdIn = new Scanner(System.in);
		
		System.out.println("정수를 입력하세요.: ");
		int n = stdIn.nextInt();
		
		if (n > 0)
			System.out.println("이 수는 양수입니다.");
		if (n < 0)
			System.out.println("이 수는 음수입니다.");
	}
}
